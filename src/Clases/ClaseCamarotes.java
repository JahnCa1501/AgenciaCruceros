
package Clases;


public class ClaseCamarotes {
    
    //Atributos
    private int idcamarote;
    private String tipo;
    private int precio;
    
    
    //Encapsulamiento
    public int getIdcamarote() {
        return idcamarote;
    }

    public void setIdcamarote(int idcamarote) {
        this.idcamarote = idcamarote;
    }

    public String getTipo() {
        return tipo;
    }

    public void setTipo(String tipo) {
        this.tipo = tipo;
    }

    public int getPrecio() {
        return precio;
    }

    public void setPrecio(int precio) {
        this.precio = precio;
    }  
    
}
