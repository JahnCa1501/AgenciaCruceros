
package Clases;


public class ClaseViajes {
    
    //Atributos
    private int id;
    private String buque;
    private String puertosalida;
    private String puertodestino;
    private String fechasalida;
    private String fecharetorno;
    private String duracion;
    
    //Encapsulamiento
    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public String getBuque() {
        return buque;
    }

    public void setBuque(String buque) {
        this.buque = buque;
    }

    public String getPuertosalida() {
        return puertosalida;
    }

    public void setPuertosalida(String puertosalida) {
        this.puertosalida = puertosalida;
    }

    public String getPuertodestino() {
        return puertodestino;
    }

    public void setPuertodestino(String puertodestino) {
        this.puertodestino = puertodestino;
    }

    public String getFechasalida() {
        return fechasalida;
    }

    public void setFechasalida(String fechasalida) {
        this.fechasalida = fechasalida;
    }

    public String getFecharetorno() {
        return fecharetorno;
    }

    public void setFecharetorno(String fecharetorno) {
        this.fecharetorno = fecharetorno;
    }

    public String getDuracion() {
        return duracion;
    }

    public void setDuracion(String duracion) {
        this.duracion = duracion;
    }
    
    
}
